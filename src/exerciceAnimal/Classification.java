package exerciceAnimal;

public class Classification {

	String embranchement;
	String classe;
	String ordre;
	String sousOrdre;
	String famille;
	String genre;
	String espece;
	
	public String getEmbranchement() {
		return embranchement;
	}
	public void setEmbranchement(String embranchement) {
		this.embranchement = embranchement;
	}
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	public String getOrdre() {
		return ordre;
	}
	public void setOrdre(String ordre) {
		this.ordre = ordre;
	}
	public String getSousOrdre() {
		return sousOrdre;
	}
	public void setSousOrdre(String sousOrdre) {
		this.sousOrdre = sousOrdre;
	}
	public String getFamille() {
		return famille;
	}
	public void setFamille(String famille) {
		this.famille = famille;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getEspece() {
		return espece;
	}
	public void setEspece(String espece) {
		this.espece = espece;
	}
	
	
}
